class Faculty:
    def __init__(self):
        self.id=""
        self.name=""
        self.courseName=""

    def setId(self,id):
        self.id=id;

    def getId(self):
        return self.id;

    def setName(self, name):
        self.name = name;

    def getName(self):
        return self.name;

    def setCourseName(self, courseName):
        self.courseName = courseName;

    def getCourseName(self):
        return self.courseName;